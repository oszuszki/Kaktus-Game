    public static void nextLvl() {
        switch(lvlCounter) {

            case 1:
                if(newLvl) {
                    x = 64;
                    y = 320;
                    movingPlatformX1 = 5;
                    movingPlatformY1 = 21;
                    platformRange = 20;
                    newLvl = false;
                }
                for(int i=0; i<map.length; i++){
                    for(int j=0; j<map[i].length; j++) {
                        map[i][j] = map1[i][j];
                        if (i == movingPlatformY1){
                            if (j == movingPlatformX1){
                                map[i][j] = 5;
                                map[i][j - 1] = 5;
                                map[i][j - 2] = 5;
                                map[i][j - 3] = 5;
                                if ((movingPlatformTime1 + 150) < System.currentTimeMillis()) {
                                    if (movingPlatformX1 < platformRange){
                                        movingPlatformX1 += 1;
                                        movingPlatformTime1 = System.currentTimeMillis();
                                    }
                                    else if (20 == platformRange){
                                        platformRange = 6;
                                        movingPlatformX1 += 1;
                                        movingPlatformTime1 = System.currentTimeMillis();
                                    }
                                    else if (movingPlatformX1 > platformRange){
                                        movingPlatformX1 -= 1;
                                        movingPlatformTime1 = System.currentTimeMillis();
                                    }
                                    else if (6 == platformRange){
                                        platformRange = 20;
                                        movingPlatformX1 -= 1;
                                        movingPlatformTime1 = System.currentTimeMillis();
                                    } } } }
                    }
                }
                break;

            case 2:
                if(newLvl) {
                    x = 1152;
                    y = 32;
                    arrowX1 = 35;
                    arrowY1 = 6;
                    arrowX2 = 23;
                    arrowY2 = 13;
                    newLvl = false;
                }
                for(int i=0; i<map.length; i++) {
                    for (int j = 0; j < map[i].length; j++) {
                        map[i][j] = map2[i][j];
                        if (i == arrowY1){
                            if (j == arrowX1){
                                map[i][j] = 3;
                                if ((arrowTime1 + 50) < System.currentTimeMillis()) {
                                    arrowX1 -= 1;
                                    arrowTime1 = System.currentTimeMillis();
                                    if(arrowX1 == 12)
                                        arrowX1 = 35; } } }
                        if (i == arrowY2){
                            if (j == arrowX2){
                                map[i][j] = 33;
                                if ((arrowTime2 + 50) < System.currentTimeMillis()) {
                                    arrowX2 += 1;
                                    arrowTime2 = System.currentTimeMillis();
                                    if(arrowX2 == 37)
                                        arrowX2 = 21; } } }
                    }
                }
                break;
        }
    }
