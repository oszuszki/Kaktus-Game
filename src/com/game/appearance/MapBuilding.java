    public static void nextLvl() {
        switch(lvlCounter) {
            case 1:
                if(newLvl) {
                    x = 64;
                    y = 320;
                    arrowX1 = 3;
                    arrowY1 = 10;
                    newLvl = false;
                }
                for(int i=0; i<map.length; i++){
                    for(int j=0; j<map[i].length; j++) {
                        map[i][j] = map1[i][j];

                    }
                }
                break;
            case 2:
                if(newLvl) {
                    x = 1152;
                    y = 32;
                    arrowX1 = 35;
                    arrowY1 = 6;
                    arrowX2 = 23;
                    arrowY2 = 13;
                    newLvl = false;
                }
                for(int i=0; i<map.length; i++) {
                    for (int j = 0; j < map[i].length; j++) {
                        map[i][j] = map2[i][j];
                        if (i == arrowY1){
                            if (j == arrowX1){
                                map[i][j] = 3;
                                if ((arrowTime1 + 50) < System.currentTimeMillis()) {
                                    arrowX1 -= 1;
                                    arrowTime1 = System.currentTimeMillis();
                                    if(arrowX1 == 12)
                                        arrowX1 = 35; } } }

                        if (i == arrowY2){
                            if (j == arrowX2){
                                map[i][j] = 33;
                                if ((arrowTime2 + 50) < System.currentTimeMillis()) {
                                    arrowX2 += 1;
                                    arrowTime2 = System.currentTimeMillis();
                                    if(arrowX2 == 37)
                                        arrowX2 = 21; } } }
                    }
                }
                break;
        }
    }
